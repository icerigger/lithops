function enc(n,d)
  if n == 1 then                                                    
    preset = preset + d
    if preset < 1 then
      preset = 0
    elseif preset > 14 then
      preset = 14
    end
    if preset > 0 then                                                
      audio:rev_on ()
      params:set('rev_return_level', honeycomb[preset][1])
      params:set('rev_pre_delay', honeycomb[preset][2])
      params:set('rev_lf_fc', honeycomb[preset][3])
      params:set('rev_low_time', honeycomb[preset][4])
      params:set('rev_mid_time', honeycomb[preset][5])
      params:set('rev_hf_damping', honeycomb[preset][6])
    else
      audio:rev_off ()
    end
    redraw()
  end
  
  if n == 2 and loopflag == 1 then
    taperate = taperate + d
    if taperate > 10 then
      taperate = 10
    elseif taperate < 0 then
      taperate = 0
    end
    if loopflag == 1 then
      softcut.rate(1,taperate/10)
    end
  end
  
  if n == 3 and loopflag == 1 then
    if d > 0 then
      for i = 1, math.random(1,tape_length*5) do
        softcut.pre_level(1,math.random(0, 1))
        rand_start = tapeloop + math.random(tape_length)
        rand_length = math.random(tape_length)
        rand_dest = tapeloop + math.random(tape_length)-rand_length
        softcut.buffer_copy_mono(2,1,rand_start,rand_dest,rand_length,0,0,math.random(-1,0))
      end
    elseif d < 1 then
      softcut.buffer_copy_mono(2,1,tapeloop,tapeloop,tape_length+1,0,0,0)
    end
  end
end

function key(n,z)
  if n == 1 then
    if z == 1 then
      down_time = util.time()
    else
      hold_time = util.time() - down_time
      if hold_time > 1 then
        tapesprite = tapesprite + 1
        if tapesprite == 2 then
          tapesprite = 0
        end
      end
    end
  end
  
  
  if n == 2 then
    if z == 1 and recflag == 0 then
      softcut.buffer_clear(1)
      softcut.rec(1,1)
      softcut.rec(2,1)
      recflag = 1
      genflag = 1 
      loopflag = 1 
    elseif z == 1 and recflag == 1 then
      softcut.rec(1,0)
      softcut.rec(2,0)
      softcut.play(1,1)
      recflag = 0
      loopflag = 1
    end
  end
  
  if n == 3 then
    if z == 1 then
      down_time = util.time()
    else
      hold_time = util.time() - down_time
      if hold_time > .5 and shift == 0 then
        ooo = util.scandir(_path.tape)
        xxx = ooo[math.random(tab.count(ooo)-1)]
        zzz = _path.tape..xxx
        ch, samples, samplerate = audio.file_info(zzz)
        duration = (samples / samplerate) - tape_length
        tapeloop = math.random(math.floor(duration + .5))
        softcut.buffer_clear()
        for i = 1, 2 do
          softcut.buffer_read_mono(_path.tape..xxx,0,1,-1,1,i)
          softcut.enable(i,1)
          softcut.buffer(i,i)
          softcut.level(i,1)
          softcut.loop(i,1)
          softcut.loop_start(i,tapeloop)
          softcut.loop_end(i,tapeloop + tape_length)
          softcut.position(i,1)
          softcut.rate(i,1)
          softcut.rec(i,0)
          softcut.play(1,1)
        end
        genflag = 1
        loopflag = 1
        taperate = 10
      else
        if loopflag == 1 and taperate > 0 then
          softcut.play(1,0)
          loopflag = 0
          recflag = 0
        elseif loopflag == 0 then
          if genflag == 1 then
            softcut.play(1,1)
            loopflag = 1
          end
        end
      end
    end
  end
end

