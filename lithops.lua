          screen.pixel(x+34,y+11)
          screen.fill()
        end
      elseif aaa == 2 and tapesprite == 1 then
         if cassette3[y][x] == 1 then
          screen.level(4)
          screen.pixel(x+34,y+11)
          screen.fill()
        elseif cassette3[y][x] == 2 then
          screen.level(15)
          screen.pixel(x+34,y+11)
          screen.fill()
        end
      end
    end
  end
  screen.update()
  if loopflag == 1 and taperate > 0 then
    aaa = aaa + 1
    if aaa == 3 then
      aaa = 0
    end
  end
end

