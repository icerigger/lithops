function key(n,z)
  if n == 1 then
    if z == 1 then
      shift = 1
    end
  end
  if n == 2 and z == 1 then
    if loop_flag == 1 then
      softcut.play(1,0)
      loop_flag = 0
    else
      softcut.play(1,1)
      loop_flag = 1
    end
  end
  if n == 3 and shift == 0 then
    if z == 1 then
      down_time = util.time()
    else
      hold_time = util.time() - down_time
      if hold_time > 1 then
        if loop_flag == 1 then
          tape_loop = math.random(util.round(duration,1))
          softcut.buffer_clear()
          for i = 1, 2 do
            softcut.enable(i,1)
            softcut.buffer(i,i)
            softcut.level(i,1)
            softcut.loop(i,1)
            softcut.loop_start(i,tape_loop)
            softcut.loop_end(i,tape_loop + tape_length)
            softcut.position(i,1)
            softcut.rate(i,1)
            softcut.buffer_read_mono(tape_selection,0,1,-1,1,i)
          end
          softcut.play(1,1)
        end
      else
        loop_flag = 1
        tape_rate = 10
        tape_location = _path.tape
        tape_collection = util.scandir(tape_location)
        repeat
          tape_selection = tape_collection[math.random(tab.count(tape_collection))]
        until tape_selection ~= "index.txt"
        tape_selection = tape_location..tape_selection
        ch, samples, samplerate = audio.file_info(tape_selection)
        duration = (samples / samplerate) - tape_length
        tape_loop = math.random(util.round(duration,1))
        softcut.buffer_clear()
        for i = 1, 2 do
          softcut.enable(i,1)
          softcut.buffer(i,i)
          softcut.level(i,1)
          softcut.loop(i,1)
          softcut.loop_start(i,tape_loop)
          softcut.loop_end(i,tape_loop + tape_length)
          softcut.position(i,1)
          softcut.rate(i,1)
          softcut.buffer_read_mono(tape_selection,0,1,-1,1,i)
        end
        softcut.play(1,1)
      end
    end
  elseif n == 3 and shift == 1 then
    if z == 1 then
      tape_save = 0
      repeat
        tape_save = tape_save + 1
        tape_overwrite = util.file_exists (_path.audio.."lithops/lithopsloop"..tape_save..".wav")
      until tape_overwrite == false
      softcut.buffer_write_mono (_path.audio.."lithops/".."lithopsloop"..tape_save..".wav",tape_loop,tape_length,0)
      print (_path.audio.."lithops/".."lithopsloop"..tape_save..".wav")
    end
  end
end

function enc (n,d)
  if n == 1 then                                                    
    preset = preset + d
    if preset < 1 then
      preset = 0
    elseif preset > 14 then
      preset = 14
    end
    if preset > 0 then                                                
      audio:rev_on ()
      params:set('rev_return_level', honeycomb[preset][1])
      params:set('rev_pre_delay', honeycomb[preset][2])
      params:set('rev_lf_fc', honeycomb[preset][3])
      params:set('rev_low_time', honeycomb[preset][4])
      params:set('rev_mid_time', honeycomb[preset][5])
      params:set('rev_hf_damping', honeycomb[preset][6])
    else
      audio:rev_off ()
    end
    redraw()
  end
  if n == 2 and loop_flag == 1 then
    tape_rate = tape_rate + d
    if tape_rate > 20 then
      tape_rate = 20
    elseif tape_rate < 0 then
      tape_rate = 0
    end
    softcut.rate(1,tape_rate/10)
  end
  if n == 3 and loop_flag == 1 then
    if d > 0 then
      for i = 1, math.random(tape_length * 5) do
        rand_start = tape_loop + math.random(tape_length)
        rand_length = math.random(tape_length) - math.random()
        rand_dest = tape_loop + rand_length
        softcut.buffer_copy_mono(2,1,rand_start,rand_dest,rand_length,math.random(),math.random(),math.random(-1,0))
      end
    elseif d < 0 then
      softcut.buffer_copy_mono(2,1,tape_loop,tape_loop,-1,0,0,0)
      softcut.rate(1,1)
      tape_rate = 10
    end
  end
end

function redraw()
  screen.clear()
  for x = 1, 60 do
    for y = 1 + (tape_sprite * 38), 38 + (tape_sprite * 38) do
      if sprites[y][x] == 1 then
        screen.level(4)
        screen.pixel(x + 34, y - (tape_sprite * 38) + 11)
      elseif sprites[y][x] == 2 then
        screen.level(15)
        screen.pixel(x + 34, y - (tape_sprite * 38) + 11)
      end
    screen.fill()
    end
  end
  screen.update()
  if loop_flag == 1 and tape_rate > 0 then
    tape_sprite = tape_sprite + 1
    if tape_sprite % 3 == 0 then
      tape_sprite = tape_sprite - 3
    end
  end
end

function cleanup()
  audio:rev_off ()
